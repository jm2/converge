// advapi32.dll provides AuditQuerySystemPolicy/AuditSetSystemPolicy for
// reading and writing advanced audit policy without auditpol.exe.
var (
	modAdvapi32 = windows.NewLazySystemDLL("advapi32.dll")

	procAuditQuerySystemPolicy = modAdvapi32.NewProc("AuditQuerySystemPolicy")
	procAuditSetSystemPolicy   = modAdvapi32.NewProc("AuditSetSystemPolicy")
	procAuditFree              = modAdvapi32.NewProc("AuditFree")
)

const (
	auditingSuccess           = 0x1
	auditingFailure           = 0x2
	auditingSuccessAndFailure = auditingSuccess | auditingFailure
	auditingNone              = 0x0
)

// auditPolicyInformation maps to AUDIT_POLICY_INFORMATION from the Win32 API.
type auditPolicyInformation struct {
	AuditSubCategoryGuid windows.GUID
	AuditingInformation  uint32
	AuditCategoryGuid    windows.GUID
}

// Check queries the current audit flags for the subcategory and compares against desired.
func (a *AuditPolicy) Check(_ context.Context) (*extensions.State, error) {
	guid, ok := subcategoryGUIDs[strings.ToLower(a.Subcategory)]
	if !ok {
		return nil, fmt.Errorf("unknown audit subcategory: %q", a.Subcategory)
	}

	var pPolicy *auditPolicyInformation
	ret, _, err := procAuditQuerySystemPolicy.Call(
		uintptr(unsafe.Pointer(&guid)),
		1,
		uintptr(unsafe.Pointer(&pPolicy)),
	)
	if ret == 0 {
		return nil, fmt.Errorf("AuditQuerySystemPolicy: %w", err)
	}
	defer procAuditFree.Call(uintptr(unsafe.Pointer(pPolicy)))

	current := pPolicy.AuditingInformation
	desired := a.desiredFlags()

	if current == desired {
		return &extensions.State{InSync: true}, nil
	}

	return &extensions.State{
		InSync: false,
		Changes: []extensions.Change{{
			Property: a.Subcategory,
			From:     flagsToString(current),
			To:       flagsToString(desired),
			Action:   "modify",
		}},
	}, nil
}

// Apply sets the audit flags for the subcategory via AuditSetSystemPolicy.
func (a *AuditPolicy) Apply(_ context.Context) (*extensions.Result, error) {
	guid, ok := subcategoryGUIDs[strings.ToLower(a.Subcategory)]
	if !ok {
		return nil, fmt.Errorf("unknown audit subcategory: %q", a.Subcategory)
	}

	policy := auditPolicyInformation{
		AuditSubCategoryGuid: guid,
		AuditingInformation:  a.desiredFlags(),
	}

	ret, _, err := procAuditSetSystemPolicy.Call(
		uintptr(unsafe.Pointer(&policy)),
		1,
	)
	if ret == 0 {
		return nil, fmt.Errorf("AuditSetSystemPolicy: %w", err)
	}

	return &extensions.Result{Changed: true, Status: extensions.StatusChanged, Message: "set"}, nil
}

func (a *AuditPolicy) desiredFlags() uint32 {
	var flags uint32
	if a.Success {
		flags |= auditingSuccess
	}
	if a.Failure {
		flags |= auditingFailure
	}
	return flags
}

func flagsToString(f uint32) string {
	switch f {
	case auditingNone:
		return "No Auditing"
	case auditingSuccess:
		return "Success"
	case auditingFailure:
		return "Failure"
	case auditingSuccessAndFailure:
		return "Success and Failure"
	default:
		return fmt.Sprintf("unknown(%d)", f)
	}
}

